package parsingJson;

public class Person {
	
	private String email;
	private String firstName;
	private String lastName;
	private int age;
	
	private Address address;
	
	
	
	public Address getAddress() {
		return address;
	}
	
	public String getEmail() {
		return email;
	}
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public int getAge() {
		return age;
	}

	
	
	
}
